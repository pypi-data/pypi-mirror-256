class L1C:
    """Class for reading
        NASA PACE Level 1C data files.
        
    """

    def __init__(self):
        """Initializes the class."""
        self.instrument = 'HARP2'   # Default instrument

    def add(self, x, y):
        """Adds two numbers."""
        return x + y

    def multiply(self, x, y):
        """Multiplies two numbers."""
        return x * y

